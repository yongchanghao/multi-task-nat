    @classmethod
    def build_model(cls, args, task):
        """Build a new model instance."""

        # make sure all arguments are present in older models
        base_architecture(args)

        if args.encoder_layers_to_keep:
            args.encoder_layers = len(args.encoder_layers_to_keep.split(","))
        if args.decoder_layers_to_keep:
            args.decoder_layers = len(args.decoder_layers_to_keep.split(","))

        if getattr(args, 'max_source_positions', None) is None:
            args.max_source_positions = DEFAULT_MAX_SOURCE_POSITIONS
        if getattr(args, 'max_target_positions', None) is None:
            args.max_target_positions = DEFAULT_MAX_TARGET_POSITIONS

        src_dict, tgt_dict = task.source_dictionary, task.target_dictionary

        def build_embedding(dictionary, embed_dim, path=None):
            num_embeddings = len(dictionary)
            padding_idx = dictionary.pad()
            emb = Embedding(num_embeddings, embed_dim, padding_idx)
            # if provided, load from preloaded dictionaries
            if path:
                embed_dict = utils.parse_embedding(path)
                utils.load_embedding(embed_dict, dictionary, emb)
            return emb

        if args.share_all_embeddings:
            if src_dict != tgt_dict:
                raise ValueError('--share-all-embeddings requires a joined dictionary')
            if args.encoder_embed_dim != args.decoder_embed_dim:
                raise ValueError(
                    '--share-all-embeddings requires --encoder-embed-dim to match --decoder-embed-dim')
            if args.decoder_embed_path and (
                    args.decoder_embed_path != args.encoder_embed_path):
                raise ValueError('--share-all-embeddings not compatible with --decoder-embed-path')
            encoder_embed_tokens = build_embedding(
                src_dict, args.encoder_embed_dim, args.encoder_embed_path
            )
            decoder_embed_tokens = encoder_embed_tokens
            args.share_decoder_input_output_embed = True
        else:
            encoder_embed_tokens = build_embedding(
                src_dict, args.encoder_embed_dim, args.encoder_embed_path
            )
            decoder_embed_tokens = build_embedding(
                tgt_dict, args.decoder_embed_dim, args.decoder_embed_path
            )
        at_encoder = cls.build_encoder(args, src_dict, encoder_embed_tokens)
        nat_encoder = at_encoder if args.share_encoder else cls.build_encoder(args, src_dict, encoder_embed_tokens)
        
        at_decoder = cls.build_at_decoder(args, tgt_dict, decoder_embed_tokens)
        nat_decoder = cls.build_nat_decoder(args, tgt_dict, decoder_embed_tokens)
        if args.share_decoder:
             nat_decoder.layers = at_decoder.layers

        at_model = TransformerModel(args, at_encoder, at_decoder)
        nat_model = CMLMNATransformerModel(args, nat_encoder, nat_decoder)
        return cls(args, at_model, nat_model)

    @classmethod
    def build_encoder(cls, args, src_dict, embed_tokens):
        encoder = TransformerEncoder(args, src_dict, embed_tokens)
        if getattr(args, "apply_bert_init", False):
            encoder.apply(init_bert_params)
        return encoder

    @classmethod
    def build_at_decoder(cls, args, tgt_dict, embed_tokens):
        at_decoder = TransformerDecoder(args, tgt_dict, embed_tokens)
        if getattr(args, "apply_bert_init", False):
            at_decoder.apply(init_bert_params)
        return at_decoder

    @classmethod
    def build_nat_decoder(cls, args, tgt_dict, embed_tokens):
        nat_decoder = NATransformerDecoder(args, tgt_dict, embed_tokens)
        if getattr(args, "apply_bert_init", False):
            nat_decoder.apply(init_bert_params)
        return nat_decoder

    def forward(self, *args, **kwargs):
        res = (self.at_model(*args, **kwargs), self.nat_model(*args, **kwargs))
        print(res)
        return res

    def max_positions(self):
        assert self.at_model.max_positions() == self.nat_model.max_positions()
        return self.at_model.max_positions()


